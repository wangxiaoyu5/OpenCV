import cv2
import numpy as np

img = np.zeros((480, 640, 3), np.uint8)
# 划线坐标点为(x,y)
# cv2.line(img, (10, 20), (340, 280), (255, 255, 255), 5, 4)
# 参数说明：
# - img: 目标图像
# - (10, 20): 起始点坐标(x, y)
# - (340, 280): 终止点坐标(x, y)
# - (255, 255, 255): BGR颜色值(白)
# - 5: 线条粗细
# - 4: 线型标志
# cv2.line(img, (50, 60), (640, 480), (255, 255, 255), 5,16)
# 矩阵
# cv2.rectangle(img, (10, 20), (340, 280), (255, 255, 255), -1)
# 参数说明：
# - img: 目标图像
# - (10, 20): 左上角坐标
# - (340, 280): 右下角坐标
# - (255, 255, 255): BGR颜色值
# - -1: 线条粗细(-1表示填充)
# 圆
# cv2.circle(img, (300, 300), 100, (255, 255, 255))
# 参数说明：
# - img: 目标图像
# - (300, 300): 圆心坐标
# - 100: 半径
# - (255, 255, 255): BGR颜色值
# 椭圆
# 顺时针 0从右边开始
# cv2.ellipse(img, (300, 300), (100, 50), 0, 0, 360, (255, 255, 255), -1)
# 参数说明：
# - img: 目标图像
# - (300, 300): 中心点坐标
# - (100, 50): 长轴和短轴半径
# - 0: 旋转角度
# - 0: 起始角度
# - 360: 结束角度
# - (255, 255, 255): BGR颜色值
# - -1: 线条粗细(-1表示填充)
# 多边形 必须是带符号32位
# 多边形顶点必须使用int32类型
pts = np.array([[10, 20], [340, 280], [340, 460], [10, 460]], np.int32)
# 参数说明：
# - [[10, 20], [340, 280], [340, 460], [10, 460]]: 顶点坐标列表
# - np.int32: 数据类型要求
cv2.polylines(img, [pts], True, (255, 255, 255))
# 参数说明：
# - img: 目标图像
# - [pts]: 顶点数组的列表
# - True: 是否闭合多边形(首尾相连)
# - (255, 255, 255): BGR颜色值
# 填充多边形
cv2.fillPoly(img, [pts], (0, 0, 255))
# 参数说明：
# - img: 目标图像
# - [pts]: 顶点数组的列表
# - (0, 0, 255): BGR颜色值(红色)
# 绘制文本
cv2.putText(img, "hello world", (10, 400), cv2.FONT_HERSHEY_SIMPLEX, 1, (255, 255, 255))
# 参数说明：
# - img: 目标图像
# - "hello world": 要绘制的文本字符串
# - (10, 400): 文本基线起始坐标
# - cv2.FONT_HERSHEY_SIMPLEX: 字体类型
# - 1: 字体大小比例
# - (255, 255, 255): BGR颜色值

cv2.imshow("img", img)
cv2.waitKey(0)
cv2.destroyAllWindows()